package com.example.assignment4

class Product {
    var Title:String
    var Price:Double
    var color:String
    var image:Int
    var itemID:Int
    var Description:String

    constructor(
        Title: String,
        Price: Double,
        color: String,
        image: Int,
        itemID: Int,
        Description: String
    ) {
        this.Title = Title
        this.Price = Price
        this.color = color
        this.image = image
        this.itemID = itemID
        this.Description = Description
    }



}